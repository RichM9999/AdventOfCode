            {
                var item = patterns[p];
                var pattern = new List<string>(item.Pattern);

                var foundNewReflection = false;

                for (var r = 0; r < pattern.Count; r++)
                {
                    for (var c = 0; c < pattern[0].Length; c++)
                    {
                        var replace = item.Pattern[r][c] == '#' ? "." : "#";

                        var unsmudgedPattern = new List<string>(item.Pattern);
                        unsmudgedPattern[r] = unsmudgedPattern[r].Remove(c, 1).Insert(c, replace);

                        if (ReflectionFound(unsmudgedPattern, 100, out long patternValue, out int reflectionRow, 'H', item.ReflectionType, item.ReflectionRow))
                        {
                            total += patternValue;
                            foundNewReflection = true;
                        }

                        if (foundNewReflection)
                            break;
                    }

                    if (foundNewReflection)
                        break;
                }

                if (!foundNewReflection)
                {
                    pattern = new List<string>(item.RotatedPattern);

                    for (var r = 0; r < pattern.Count; r++)
                    {
                        for (var c = 0; c < pattern[0].Length; c++)
                        {
                            var replace = item.RotatedPattern[r][c] == '#' ? "." : "#";

                            var unsmudgedPattern = new List<string>(item.RotatedPattern);
                            unsmudgedPattern[r] = unsmudgedPattern[r].Remove(c, 1).Insert(c, replace);

                            if (ReflectionFound(unsmudgedPattern, 1, out long patternValue, out int reflectionRow, 'V', item.ReflectionType, item.ReflectionRow))
                            {
                                total += patternValue;
                                foundNewReflection = true;
                            }

                            if (foundNewReflection)
                                break;
                        }

                        if (foundNewReflection)
                            break;
                    }
                }

                if (!foundNewReflection)
                {
                    throw new Exception();
                }
            }
            
            Console.WriteLine($"Total value of all unsmudged reflections: {total}");
            // 29341
            Console.WriteLine($"{(DateTime.Now - start).TotalMilliseconds}ms");

        }

        bool ReflectionFound(List<string> pattern, long valueFactor, out long patternValue, out int row, char currentType, char previousType, int previousRow)
        {
            patternValue = 0;

            // Check for horizontal reflection
            for (row = 1; row < pattern.Count; row++)
            {
                var take = row;
                var skip = 0;
                if (take * 2 > pattern.Count)
                {
                    take = pattern.Count - row;
                    skip = row - take;
                }
                var top = string.Join("|", pattern.Skip(skip).Take(take));

                var bottomList = new List<string>();
                for (var mirror = take; mirror >= 1; mirror--)
                {
                    bottomList.Add(pattern[mirror + row - 1]);
                }

                var bottom = string.Join("|", bottomList);

                if (top == bottom && (currentType != previousType || row != previousRow))
                {
                    patternValue = row * valueFactor;
                    return true;
                }
            }

            return false;
        }

        void AddPattern(string row)
        {
            if (row.Length > 0)
            {
                currentPattern.Add(row);
            }
            else if (currentPattern.Count > 0)
            {
                // rotate pattern
                var rotatedPattern = new List<string>();

                for (var i = 0; i < currentPattern[0].Length; i++)
                {
                    var rotated = string.Empty;

                    foreach (var patternRow in currentPattern)
                    {
                        rotated += patternRow[i];
                    }

                    rotatedPattern.Add(rotated);
                }

                patterns.Add(new PatternInfo { Pattern = currentPattern, RotatedPattern = rotatedPattern, ReflectionRow = -1, ReflectionType = 'X' });

                currentPattern = [];
            }
        }

