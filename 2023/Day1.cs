        public void Run()
        {
            var start = DateTime.Now;

            GetData();

            numbers = new Dictionary<string, int>()
            {
                { "on", 1},
                { "tw", 2},
                { "thre", 3},
                { "four", 4},
                { "fiv", 5},
                { "six", 6},
                { "seve", 7},
                { "eigh", 8},
                { "nin", 9}
            };

            long sum = 0;

            Regex test = new(@"(\d)");

            foreach (var line in lines)
            {
                var matches = test.Matches(line);

                int.TryParse(matches.First().Value, out int first);
                int.TryParse(matches.Last().Value, out int last);
                sum += (first * 10) + last;
            }

            Console.WriteLine($"Sum: {sum}");
            //52974
            Console.WriteLine($"{(DateTime.Now - start).TotalMilliseconds}ms");

            sum = 0;
            test = new(@"(\d)|(on(?=e))|(tw(?=o))|(thre(?=e))|(four)|(fiv(?=e))|(six)|(seve(?=n))|(eigh(?=t))|(nin(?=e))");

            foreach (var line in lines)
            {
                var matches = test.Matches(line);

                var first = ParseMatch(matches.First().Value);
                var last = ParseMatch(matches.Last().Value);

                sum += (first * 10) + last;
            }

            Console.WriteLine($"Sum: {sum}");
            //53340
            Console.WriteLine($"{(DateTime.Now - start).TotalMilliseconds}ms");
        }

        int ParseMatch(string match)
        {
            if (int.TryParse(match, out int value))
            {
                return value;
            }
            else
            {
                return numbers[match];
            }
        }


