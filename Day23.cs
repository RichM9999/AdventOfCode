        bool IsClique(List<string> nodes)
        {
            for (int n1 = 0; n1 < nodes.Count - 1; n1++)
            {
                for (int n2 = n1 + 1; n2 < nodes.Count; n2++)
                {
                    var node1 = nodes[n1];
                    var node2 = nodes[n2];

                    if (!graph[node1].Contains(node2))
                    { 
                        // Nodes are not adjacent - not a clique
                        return false; 
                    }
                }
            }

            // All nodes are adjacent
            return true;
        }
