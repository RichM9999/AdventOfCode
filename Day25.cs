        public void Run()
        {
            var start = DateTime.Now;

            GetData();

            var pairFits = 0;

            foreach (var Lock in locks)
            {
                foreach (var Key in keys)
                {
                    var fits = true;

                    for (var col = 0; col < 5; col++)
                    {
                        if (Lock[col] + Key[col] > 5)
                        {
                            fits = false;
                            break;
                        }
                    }

                    pairFits += fits ? 1 : 0;
                }
            }

            Console.WriteLine($"Lock/key pairs that fit: {pairFits}");
            // 3483
            Console.WriteLine($"{(DateTime.Now - start).TotalMilliseconds}ms");
        }

        void GetData()
        {
            GetInput();

            bool isKey = true;
            bool isFirstLine = true;
            List<string> item = [];

            foreach (var line in input)
            {
                if (line.Trim().Length == 0)
                {
                    var columns = GetColumns(item);
                    if (isKey)
                    {
                        keys.Add(columns);
                    }
                    else
                    {
                        locks.Add(columns);
                    }
                    
                    item = [];
                    isKey = true;
                    isFirstLine = true;
                    continue;
                }

                if (isFirstLine)
                {
                    if (line == "#####")
                    {
                        isKey = false;
                    }
                    isFirstLine = false;
                }

                item.Add(line);
            }
        }

        byte[] GetColumns(List<string> item)
        {
            byte[] columns = [0, 0, 0, 0, 0];

            for (var col = 0; col < 5; col++)
            {
                for (var row = 1; row < 6; row++)
                {
                    var value = item[row][col];

                    columns[col] += (byte)(item[row][col] == '#' ? 1 : 0);
                }
            }
            return columns;
        }

