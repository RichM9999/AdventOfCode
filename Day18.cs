        int BFS()
        {
            Dictionary<Coordinate, Coordinate> parents = new Dictionary<Coordinate, Coordinate>();
            Dictionary<Coordinate, int> distances = new Dictionary<Coordinate, int>();
            Dictionary<Coordinate, List<Coordinate>> edges = new Dictionary<Coordinate, List<Coordinate>>();

            // Start at 0,0
            Coordinate start = (0,0);

            // End at bottom-right corner
            Coordinate end = (mapSizeX - 1, mapSizeY - 1);

            // Build neighboring edges where walls don't exist
            for (var x = 0; x < mapSizeX; x++)
            {
                for (var y = 0; y < mapSizeY; y++)
                {
                    if (map[x, y] == '#')
                        continue;

                    var neighbors = new List<Coordinate>();

                    // Check left
                    if (x > 0 && map[x - 1, y] != '#')
                    {
                        neighbors.Add((x - 1, y));
                    }

                    // Check right
                    if (x < mapSizeX - 1 && map[x + 1, y] != '#')
                    {
                        neighbors.Add((x + 1, y));
                    }

                    // Check up
                    if (y > 0 && map[x, y - 1] != '#')
                    {
                        neighbors.Add((x, y - 1));
                    }

                    // Check down
                    if (y < mapSizeY - 1 && map[x, y + 1] != '#')
                    {
                        neighbors.Add((x, y + 1));
                    }

                    edges.Add((x, y), neighbors);
                }
            }

            // Queue to store the nodes in the order they are
            // visited
            Queue<Coordinate> queue = new Queue<Coordinate>();
            // Mark the distance of the source node as 0
            distances[start] = 0;
            // Push the source node to the queue
            queue.Enqueue(start);

            // Iterate until the queue is not empty
            while (queue.Count > 0)
            {
                // Dequeue the node at the front of the queue
                Coordinate node = queue.Dequeue();

                // Explore all the neighbors of the current node
                foreach (Coordinate neighbor in edges[node])
                {
                    // Check if the neighboring node is not visited
                    if (!distances.ContainsKey(neighbor))
                    {
                        // Mark the current node as the parent
                        // of the neighboring node
                        parents[neighbor] = node;
                        // Mark the distance of the neighboring
                        // node as the distance of the current
                        // node + 1
                        distances[neighbor] = distances[node] + 1;
                        // Insert the neighboring node to the
                        // queue
                        queue.Enqueue(neighbor);
                    }
                }
            }

            // Return distance to end, unless not found
            if (distances.ContainsKey(end))
                return distances[end];
            else
                return -1;
        }

