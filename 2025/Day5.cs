        private long Part2()
        {
            // Make ranges non-overlapping
            for (var r = 0; r < freshRanges.Count; r++)
            {
                var range = freshRanges[r];

                for (var o = 0; o < freshRanges.Count; o++)
                {
                    // Skip if comparing to self or other range is already disabled
                    if (r == o || freshRanges[o].low == 0)
                        continue;

                    var other = freshRanges[o];

                    // other range encapsulates entirety of range
                    // so disable range
                    //   ooooo
                    //    rrr
                    if (other.low <= range.low && other.high >= range.high)
                    {
                        range.low = 0;
                        range.high = 0;
                        freshRanges[r] = range;
                        // stop checking other ranges since this range is now disabled
                        break;
                    }

                    // range encapsulates entirety of other range
                    // so disable other range
                    //   rrrrr
                    //    ooo
                    if (other.low <= range.low && other.high >= range.high)
                    {
                        other.low = 0;
                        other.high = 0;
                        freshRanges[o] = other;
                        continue;
                    }

                    // other range overlaps range and extends past
                    // trim range to start before other range
                    //   ooooo
                    // rrrrr
                    if (other.low >= range.low
                            && other.high >= range.high
                            && other.low <= range.high
                            && other.high >= range.low)
                    {
                        range.high = other.low - 1;
                        freshRanges[r] = range;
                        continue;
                    }

                    // other range overlaps range and starts before
                    // trim range to start after other range
                    //   ooooo
                    //     rrrrr
                    if (other.low <= range.low
                            && other.high <= range.high
                            && other.high >= range.low
                            && other.low <= range.high)
                    {
                        range.low = other.high + 1;
                        freshRanges[r] = range;
                        continue;
                    }
                }
            }

            // After making all ranges non-overlapping, get count of numbers (high - low plus one) in valid ranges
            return freshRanges.Where(r => r.low != 0).Sum(r => r.high - r.low + 1);
        }

