        private long Part1()
        {
            long fits = 0;

            foreach (var region in regions)
            {
                var pieces = region.shape0Count +
                    region.shape1Count +
                    region.shape2Count +
                    region.shape3Count +
                    region.shape4Count;

                // Turns out, only actually need to determine if the region square area would fit the number of 3x3 pieces
                if (region.width * region.length >= pieces * 9)
                    fits++;
            }

            return fits;
        }

