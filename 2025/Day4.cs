        public int GetRemovableRollCount(bool unlimitedPasses)
        {
            var neighborLimit = 3;
            var accessibleSpaces = 0;
            List<Coordinate> toRemove = [];

            do
            {
                toRemove.Clear();

                // Enumerate map
                for (var x = 0; x < mapSizeX; x++)
                {
                    for (var y = 0; y < mapSizeY; y++)
                    {
                        // Make coordinate
                        var space = new Coordinate(x, y);

                        // If space is empty, continue
                        if (map[space] == '.')
                            continue;

                        // If space has fewer than allowed neighbors...
                        if (NumberOfNeighboringRolls(space) <= neighborLimit)
                        {
                            if (unlimitedPasses)
                            {
                                // Add to removal list
                                toRemove.Add(space);
                            }
                            // Increment count of accessible spaces
                            accessibleSpaces++;
                        }
                    }
                }

                // If multiple passes, clear found accessible spaces in map
                if (unlimitedPasses)
                {
                    foreach (var space in toRemove)
                    {
                        map[space] = '.';
                    }
                }
            // Continue until no new accessible spaces found, or single-pass
            } while (unlimitedPasses && toRemove.Count > 0);

            return accessibleSpaces;
        }

        public int NumberOfNeighboringRolls(Coordinate space)
        {
            var neighborCount = 0;

            // Enumerate through all 8 neighboring coordinates
            foreach (var (neighborX, neighborY) in neighbors)
            {
                // Get neighboring coordinate
                var neighborCoordinate = new Coordinate(space.x + neighborX, space.y + neighborY);

                // Ignore out-of-range neighbors
                if (neighborCoordinate.x < 0 || neighborCoordinate.y < 0 || neighborCoordinate.x >= mapSizeX || neighborCoordinate.y >= mapSizeY)
                    continue;

                // Check if neighbor has a roll of paper
                if (map[neighborCoordinate] == '@')
                    neighborCount++;
            }

            return neighborCount;
        }

        void AddMapRow(int row, string data)
        {
            for (var x = 0; x < data.Length; x++)
            {
                var space = data[x];
                map.Add((x, row), space);
            }
        }

